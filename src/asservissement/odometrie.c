TaskHandle_t odometrie(void)
{
    /* echant */
TickType_t xLastWakeUp;
    /*valeur codeurs */
    float codeur_g, codeur_d;
    float angle=0,somme=0;
    float ang_vit[2]={0,0},dist_vit[2]={0,0};
    float ang_acc=0,dist_acc=0;
    float x=0,y=0;
    /*init driver decoder quadra */
    init_mtclk();

    /* init variable mesure vitesse */
    PORT2.PDR.BIT.B2 = 1;
    PORT2.PMR.BIT.B2 = 0;
    PORT2.PODR.BIT.B2 =1;

    /* on démarre les codeurs */
    mtclk_start();
    /* on démarre les décodeurs */
    xLastWakeUp = xTaskGetTickCount();
    for(;;)
    {
        vTaskDelayUntil(&xLastWakeUp, TPS_ECH);
        #ifdef SPEED_ODO
            PORT2.PODR.BIT.B2 =1;
        #endif
        /* on récupère les valeurs codeurs */
        codeur_g = (COMPTEUR_G-INIT_COD)/RATIO_RG;
        codeur_d = -(COMPTEUR_D-INIT_COD)/RATIO_RD;
//        printf("codeur_g:%.0f codeur_d:%.0f\n\r",\
            codeur_g,codeur_d);
        /* on reset les compteurs */
        mtclk_reset();
        /* sauvegarde de n-1 */
        if(xSemaphoreTake(xOdo_mutex,portMAX_DELAY))
        {
            odo.orientation += 180*asinf((codeur_g-codeur_d)\
                        /L_ROBOT)/PI;
            odo.norme += (codeur_g+codeur_d)/2.0;
            angle = odo.orientation;
            somme = odo.norme;

            //printf("somme: %.0f mm, angle:%.1f°\n\r",odo.norme,odo.orientation);
            xSemaphoreGive(xOdo_mutex);
        }
        //y += (codeur_g-codeur_d)*somme/L_ROBOT;
        if(angle>=0 && angle <180)
        {
            y += sin_tab[(int)angle]*(somme);
            x += cos_tab[(int)angle]*(somme);
        }
        else if(angle<0 && angle > -180)
        {
            y += -sin_tab[(int)-angle]*(somme);
            x += cos_tab[(int)-angle]*(somme);
        }

  //      printf("x: %.0fmm, y: %.0fmm\n\r",x,y);
        #ifdef SPEED_ODO
            PORT2.PODR.BIT.B2 =0;
        #endif

    }
}
